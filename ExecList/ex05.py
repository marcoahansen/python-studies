
print("Este programa irá calcular o delta da equação de segundo grau formada por três coeficientes!")
a = int(input("Digite o valor do coeficiente a: "))
b = int(input("Digite o valor do coeficiente b: "))
c = int(input("Digite o valor do coeficiente c: "))

delta = b**2 - 4*a*c

print(f"O delta dessa equação é: {delta}")
